    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Comment $comment)
    {
        $validationRules = [];
        $validationRules['content'] = 'required|max:2000';
        $validationRules['articleId'] = 'required|exists:App\Models\Article,id';

        $validated = $request->validate($validationRules);

        $comment->content = $validated['content'];

        $comment->save();

        return redirect(route('articles.show.admin'))->with('success', 'Votre commentaire a bien été enregistré');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy(Comment $comment)
    {
        $comment->delete();

        return redirect()->back()->with('success', 'Le commentaire a bien été supprimé');
    }
